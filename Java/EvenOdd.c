// Program to check whether a number is even or odd
#include <stdio.h>

int main() {

    // Number to be checked
    int number = 7;

    // Check even or odd using modulus operator
    if (number % 2 == 0) {
        printf("Even Number\n");
    } else {
        printf("Odd Number\n");
    }

    return 0;
}
